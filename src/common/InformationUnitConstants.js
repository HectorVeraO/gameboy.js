const kibi = 2 ** 10;
const mebi = 2 ** 20;

export const bit = 1;
export const byte = 1 * bit;;

export const kibibit = kibi * bit;
export const Kib = kibibit;
export const Kb = Kib;

export const kibibyte = kibi * byte;
export const KiB = kibibyte;
export const KB = KiB;

export const mebibit = mebi * bit;
export const Mb = mebibit;
export const Mib = Mb;

export const mebibyte = mebi * byte;
export const MiB = mebibyte;
export const MB = MiB;
